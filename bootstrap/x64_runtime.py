    x.inst("jl", LabelRef("__epx_cstr.fail"))
    x.inst("mov", R("r10"), I(0))
    x.label("__epx_cstr.loop")
    x.inst("cmp", R("r10"), R("r9"))
    x.inst("jge", LabelRef("__epx_cstr.tail"))
    x.inst("mov", R("r11"), R("r8"))
    x.inst("add", R("r11"), R("r10"))
    x.inst("movzx", R("rax"), M("r11", 0, 1))
    x.inst("test", R("rax"), R("rax"))
    x.inst("jz", LabelRef("__epx_cstr.fail"))
    x.inst("add", R("r10"), I(1))
    x.inst("jmp", LabelRef("__epx_cstr.loop"))
