    x.inst("mov", R("rcx"), M("rbp", -40))
    x.inst("mov", M("rax", 8), R("rcx"))
    x.inst("mov", R("rdx"), M("rbp", -8))
    x.inst("mov", R("rcx"), M("rdx", 8))
