"""Runtime fragments for the structured X64 backend."""

from x64 import I, M, MS, R, LabelRef, Symbol


FULL_RUNTIME = "full"


def emit_runtime_data(x64, program):
    string_globals = {}
    x64.data_zero("_written", 4)
    x64.data_zero("_heap", 8)
    x64.data_zero("_argv", 8)
    x64.data_bytes("_newline", [10])
    x64.data_bytes("_cstr_panic_prefix", list(b"panic line "))
    x64.data_bytes("_cstr_panic_suffix", list(b": invalid cstr"))
    for glob in program.globals:
        if glob.name == "argv":
            continue
        data_label = _data_label(glob.name)
        header_label = _header_label(glob.name)
        values = list(glob.init.encode("ascii")) + [0]
        string_globals[glob.name] = (header_label, data_label, len(glob.init))
        x64.data_bytes(data_label, values)
        x64.data_zero(header_label, 24)
    return string_globals


def emit_startup_hook_call(x64):
    x64.inst("sub", R("rsp"), I(32))
    x64.inst("call", Symbol("__epx_runtime_start"))
    x64.inst("add", R("rsp"), I(32))


def append_runtime_helpers(x64, policy=FULL_RUNTIME):
    if policy != FULL_RUNTIME:
        raise RuntimeError(f"unsupported X64 runtime policy: {policy}")
    _emit_runtime_start(x64)
    emit_x64_runtime_helpers(x64)


def _emit_runtime_start(x64):
    x64.label("__epx_runtime_start")
    x64.inst("push", R("rbp"))
    x64.inst("mov", R("rbp"), R("rsp"))
    x64.inst("sub", R("rsp"), I(32))
    x64.inst("call", Symbol("GetProcessHeap"))
    x64.inst("add", R("rsp"), I(32))
    x64.inst("mov", MS("_heap"), R("rax"))
    x64.inst("sub", R("rsp"), I(32))
    x64.inst("call", Symbol("__epx_argv_init"))
    x64.inst("add", R("rsp"), I(32))
    x64.inst("mov", MS("_argv"), R("rax"))
    x64.inst("pop", R("rbp"))
    x64.inst("ret")



def emit_ep_wrapper(x64, public_name, primitive_name):
    x64.label(public_name)
    x64.inst("jmp", LabelRef(primitive_name))


def emit_x64_runtime_helpers(x64):
    # Public __ep_* helper symbols are semantic-layer entry points. For now
    # these wrappers tail-jump to __epx_* x64 primitives; later commits can
    # replace individual wrappers with MIR/Epic implementations without
    # changing frontend/codegen call sites.
    emit_ep_wrapper(x64, "__ep_cstr", "__epx_cstr")
    emit_ep_wrapper(x64, "__ep_write_file", "__epx_write_file")
    emit_ep_wrapper(x64, "__ep_read_file", "__epx_read_file")
    emit_ep_wrapper(x64, "__ep_system_cmd", "__epx_system_cmd")
    emit_ep_wrapper(x64, "__ep_print_str", "__epx_print_str")
    emit_ep_wrapper(x64, "__ep_print_newline", "__epx_print_newline")
    emit_epx_alloc(x64)
    emit_epx_cstr(x64)
    emit_epx_write_file(x64)
    emit_epx_read_file(x64)
    emit_epx_system_cmd(x64)
    emit_epx_argv_init(x64)
    emit_epx_print_str(x64)
    emit_epx_print_newline(x64)
    emit_epx_slice_oob(x64)
    emit_epx_null_deref(x64)

def _data_label(name):
    return name + "_data"


def _header_label(name):
    return name + "_header"

def emit_epx_alloc(x):
    x.label("__epx_alloc")
    x.inst("push", R("rbp"))
    x.inst("mov", R("rbp"), R("rsp"))
    x.inst("mov", R("r8"), R("rcx"))
    x.inst("mov", R("rcx"), MS("_heap"))
    x.inst("mov", R("rdx"), I(8))
    x.inst("sub", R("rsp"), I(32))
    x.inst("call", Symbol("HeapAlloc"))
    x.inst("add", R("rsp"), I(32))
    x.inst("pop", R("rbp"))
    x.inst("ret")

