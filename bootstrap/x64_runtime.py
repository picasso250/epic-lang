def emit_epx_argv_init(x):
    x.label("__epx_argv_init")
    x.inst("push", R("rbp"))
    x.inst("mov", R("rbp"), R("rsp"))
    x.inst("sub", R("rsp"), I(128))
    x.inst("sub", R("rsp"), I(32))
    x.inst("call", Symbol("GetCommandLineA"))
    x.inst("add", R("rsp"), I(32))
    x.inst("mov", R("rsi"), R("rax"))
    x.inst("mov", R("rcx"), MS("_heap"))
    x.inst("mov", R("rdx"), I(8))
    x.inst("mov", R("r8"), I(24))
    x.inst("sub", R("rsp"), I(32))
    x.inst("call", Symbol("HeapAlloc"))
    x.inst("add", R("rsp"), I(32))
    x.inst("mov", M("rbp", -8), R("rax"))
    x.inst("mov", R("rcx"), I(0))
    x.inst("mov", M("rax", 8), R("rcx"))
    x.inst("mov", R("rcx"), I(64))
    x.inst("mov", M("rax", 16), R("rcx"))
    x.inst("mov", R("rcx"), MS("_heap"))
    x.inst("mov", R("rdx"), I(8))
    x.inst("mov", R("r8"), I(512))
    x.inst("sub", R("rsp"), I(32))
    x.inst("call", Symbol("HeapAlloc"))
    x.inst("add", R("rsp"), I(32))
    x.inst("mov", R("rcx"), M("rbp", -8))
    x.inst("mov", M("rcx"), R("rax"))
    x.label("__epx_argv_init.skip_ws")
    x.inst("movzx", R("rax"), M("rsi", 0, 1))
    x.inst("mov", R("rcx"), I(0))
    x.inst("cmp", R("rax"), R("rcx"))
    x.inst("jz", LabelRef("__epx_argv_init.done"))
    for value in (32, 9, 13, 10):
        x.inst("mov", R("rcx"), I(value))
        x.inst("cmp", R("rax"), R("rcx"))
        x.inst("jz", LabelRef("__epx_argv_init.advance_ws"))
    x.inst("jmp", LabelRef("__epx_argv_init.arg_start"))
    x.label("__epx_argv_init.advance_ws")
    x.inst("add", R("rsi"), I(1))
    x.inst("jmp", LabelRef("__epx_argv_init.skip_ws"))
    x.label("__epx_argv_init.arg_start")
    x.inst("mov", R("r11"), I(0))
    x.inst("mov", R("rcx"), I(34))
    x.inst("cmp", R("rax"), R("rcx"))
    x.inst("jnz", LabelRef("__epx_argv_init.start_plain"))
    x.inst("mov", R("r11"), I(1))
    x.inst("add", R("rsi"), I(1))
    x.label("__epx_argv_init.start_plain")
    x.inst("mov", M("rbp", -24), R("rsi"))
    x.inst("mov", R("r10"), I(0))
    x.label("__epx_argv_init.scan")
    x.inst("movzx", R("rax"), M("rsi", 0, 1))
    x.inst("mov", R("rcx"), I(0))
    x.inst("cmp", R("rax"), R("rcx"))
    x.inst("jz", LabelRef("__epx_argv_init.store"))
    x.inst("test", R("r11"), R("r11"))
    x.inst("jnz", LabelRef("__epx_argv_init.scan_quoted"))
    for value in (32, 9, 13, 10):
        x.inst("mov", R("rcx"), I(value))
        x.inst("cmp", R("rax"), R("rcx"))
